from scipy import stats

sample_mean = 260
standard_deviation=90
claimed_population_mean=270
sample_size=18 #t-distribution

standard_error = standard_deviation / (sample_size**0.5)
t_statistic= (sample_mean-claimed_population_mean)/standard_error

degrees_of_freedom=sample_size-1

p_value= stats.t.cdf(t_statistic,degrees_of_freedom)

print('If the CEO claim were true, what is the probability that 18 randomly selected bulbs would have an average life of no more than 260 days: ',
      p_value.round(2))